    private void popularCampos() {

        Elemento posicaoUm = ListaRank.getPosicaoUm();
        Elemento posicaoDois = ListaRank.getPosicaoDois();
        Elemento posicaoTres = ListaRank.getPosicaoTres();
        String posicaoTitulo = ListaRank.getPosicaoTitulo();

        this.telaVotoPosicaoUm.setText(posicaoUm.getNome());
        this.telaVotoPosicaoDois.setText(posicaoDois.getNome());
        this.telaVotoPosicaoTres.setText(posicaoTres.getNome());
        this.telaVotoTitulo.setText(posicaoTitulo);
    }

