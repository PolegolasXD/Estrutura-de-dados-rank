    private void telaVotoPosicaoUmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_telaVotoPosicaoUmActionPerformed
        String votoPosicaoUm = telaVotoPosicaoUm.getText();
        Elemento elementoVotado = ListaRank.getListaPosicaoUm().stream().filter(elemento -> votoPosicaoUm.equals(elemento.getNome())).findAny().orElse(null);
        elementoVotado.receberVotos();
        new ResultadoRank().setVisible(true);
        TelaVoto.this.dispose();
    }//GEN-LAST:event_telaVotoPosicaoUmActionPerformed

